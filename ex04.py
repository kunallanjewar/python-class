import datetime
dt = datetime.datetime.now()
a = int(dt.strftime("%w"))

if (a == 1) :  print("Everything comes to him who waits.")
elif (a == 2) :  print("Give credit where credit is due.")
elif (a == 3) :  print("If you pay peanuts, you get monkeys.")
elif (a == 4) :  print("Money makes the world go round.")
elif (a == 5) :  print("Prevention is better than cure.")
elif (a == 6) :  print("Stupid is as stupid does.")
else :  print("The devil looks after his own.")